package operator;

public class do_while_loop {

	public static void main(String[] args) {
		
		int a=1;
		do {
			System.out.print(a+" ");
			a++;
		} while (10>=a);
		System.out.println();
		
		for(int u=20;u>=1;u--) {                                                  //code for loop
			System.out.print(u+" ");
		}
		
		System.out.println();
		int r=1;
		
		while(r<=20) {																	// code while loop
			System.out.print(r+" ");
			++r;
		}
		System.out.println();
		int y=20;
		do {																					//  code for do while loop
			System.out.print(y+" ");
			y-=2;
		} while (y>=1);
		
			
	}
	

}
